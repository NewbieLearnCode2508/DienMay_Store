    private void DisplayPr() {
        String query = "Select * from db_hoa_don";
        try {
            PreparedStatement statement = connection.prepareStatement(query);
            ResultSet resultSet = statement.executeQuery();

            DefaultTableModel model = (DefaultTableModel) tblQLHD.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Object[] row = {
                    resultSet.getString("ma_hd"),
                    resultSet.getString("ma_nv"),
                    resultSet.getString("ma_kh"),
                    resultSet.getDate("ngay_lap"),
                    resultSet.getInt("da_thanh_toan"),
                    resultSet.getInt("con_lai"),
                    resultSet.getInt("tonggiatri"),
                    resultSet.getString("thanh_toan"),};
                model.addRow(row);
            }
            tblQLHD.setModel(model);
        } catch (SQLException ex) {
            System.out.println("Lỗi khi truy vấn dữ liệu từ cơ sở dữ liệu: " + ex.getMessage());
        }
    }

