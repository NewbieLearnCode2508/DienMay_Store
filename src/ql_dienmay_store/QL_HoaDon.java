    private void tblQLHDMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblQLHDMouseClicked
        CloseEnableTxt();
        int row = tblQLHD.getSelectedRow();
        if (row != -1) {
            String maHD = (String) tblQLHD.getValueAt(row, 0);
            String maNV = (String) tblQLHD.getValueAt(row, 1);
            String maKH = (String) tblQLHD.getValueAt(row, 2);
            String ngayLap = tblQLHD.getValueAt(row, 3).toString();
            String daThanhToan = tblQLHD.getValueAt(row, 4).toString();
            String conLai = tblQLHD.getValueAt(row, 5).toString();
            String tongGiaTri = tblQLHD.getValueAt(row, 6).toString();
            String thanhToan = tblQLHD.getValueAt(row, 7).toString();
            txtMaHD.setText(maHD);
            txtMaNV.setText(maNV);
            txtMaKH.setText(maKH);
            txtNgayThanhToan.setText(ngayLap);
            txtTienDua.setText(daThanhToan);
            txtTienConLai.setText(conLai);
            txtTongTien.setText(tongGiaTri);
            txtTinhTrang.setText(thanhToan);
        }
    }//GEN-LAST:event_tblQLHDMouseClicked

